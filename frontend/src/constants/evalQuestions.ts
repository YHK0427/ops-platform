/** 도메인 내에서 종합(self+audience 평균) 점수가 가장 낮은 문항 key 반환 */
export function getLowestQuestionInDomain(
    domain: string,
    selfByQ: Record<string, number | null>,
    audienceByQ: Record<string, number | null>,
): string | null {
    const domainQuestions = QUESTIONS_BY_DOMAIN[domain as keyof typeof QUESTIONS_BY_DOMAIN];
    if (!domainQuestions) return null;

    let lowestKey: string | null = null;
    let lowestScore = Infinity;

    for (const q of domainQuestions) {
        const s = selfByQ[q.key] ?? null;
        const a = audienceByQ[q.key] ?? null;
        let combined: number | null = null;
        if (s != null && a != null) combined = (s + a) / 2;
        else if (s != null) combined = s;
        else if (a != null) combined = a;

        if (combined != null && combined < lowestScore) {
            lowestScore = combined;
            lowestKey = q.key;
        }
    }

    return lowestKey;
}
